export class CellData {
  geometryCounts: number; // High nibble is vertex count, low nibble is triangle count.
  vertexIndex: number[] | Uint8Array; // Groups of 3 indexes giving the triangulation.
  vertexCount: number;
  triangleCount: number;

  constructor(geometryCounts: number, vertexIndex: Uint8Array) {
    this.geometryCounts = geometryCounts;
    this.vertexIndex = vertexIndex;
    this.vertexCount = CellData.getVertexCount(geometryCounts);
    this.triangleCount = CellData.getTriangleCount(geometryCounts);
  }

  static getVertexCount(geometryCounts: number) {
    return geometryCounts >> 4;
  }

  static getTriangleCount(geometryCounts: number) {
    return geometryCounts & 0x0f;
  }
}

